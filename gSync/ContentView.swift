import SwiftUI

/// Основное представление пользовательского интерфейса приложения `gSync`.
/// - Логика: Этот модуль представляет собой простую заглушку для интерфейса приложения, отображающую приветственное сообщение и иконку. В текущей версии он не содержит активной функциональности, связанной с синхронизацией файлов, и служит скорее как точка входа для отображения GUI. В будущем здесь может быть реализован полноценный интерфейс управления синхронизацией.
/// - Особенности: Использует SwiftUI для декларативного построения UI. В текущем состоянии не зависит от других модулей приложения, таких как `FolderServer` или `SyncOrchestrator`.
struct ContentView: View {
    
    /// Свойство, определяющее содержимое представления.
    /// - Возвращает: Вертикальный стек (VStack) с изображением и текстом, оформленный отступами.
    var body: some View {
        VStack {
            // Иконка глобуса, отображаемая в интерфейсе.
            Image(systemName: "globe")
                .imageScale(.large) // Устанавливает большой масштаб иконки для улучшенной видимости.
                .foregroundStyle(.tint) // Применяет текущий акцентный цвет приложения к иконке.
            
            // Приветственный текст, отображаемый под иконкой.
            Text("Hello, world!")
        }
        .padding() // Добавляет внутренние отступы вокруг содержимого для улучшения внешнего вида.
    }
}

/// Предпросмотр для разработчиков в Xcode.
/// - Используется в среде разработки для визуального тестирования интерфейса без запуска приложения.
#Preview {
    ContentView()
}

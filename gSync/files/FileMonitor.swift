    /// В будущем будет получать реальные файлы из папки.
    func getFiles(forPath path: String) -> [SyncingFile] {
        return []
    }

    /// Добавляет мониторинг для указанного пути.
    /// Сейчас это заглушка, только логирует действие без реальной работы.
    /// В будущем будет настраивать `DispatchSource` для отслеживания изменений.
    func addMonitor(forPath path: String) {
        Logger.shared.log("Добавлен мониторинг для \(path) (заглушка)")
    }
}
